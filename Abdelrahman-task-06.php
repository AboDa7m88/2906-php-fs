<?php

//function to take 2 num's and return the sum 
function addNumbers($n1,$n2){
    return $n1+$n2;
}

//function to take 3 num's and return the mul
function multiply ($a,$b,$c){
    return $a*$b*$c;
}

//function to take 2 num's and return the divide
function divide($a,$b) {
    return $a/$b;
}